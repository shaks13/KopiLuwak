#ifndef COMMON_MACRO_H
#define COMMON_MACRO_H

/*===========================================================================================================
						macro definition
===========================================================================================================*/


/* GET LSB AND MSB OF A WORD */
#define COMMON_GETLSB(ui16data)					(ui16data & 0x00FF)
#define COMMON_GETMSB(ui16data)					((ui16data & 0xFF00)>>8)

#ifndef MIN
#  define MIN(X, Y) (((X) < (Y)) ? (X) : (Y))
#endif
#ifndef MAX
#  define MAX(X, Y) (((X) > (Y)) ? (X) : (Y))
#endif

#endif


